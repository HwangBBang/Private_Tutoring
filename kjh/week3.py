# 변수와 초기화!

# a|_10_|
a = 10  # 박스(상자)에 데이터를 넣어요
# 상자이름이 a인 곳에 10 을 넣었어요

# b |_-10_|
b = -10
# 상자 b 인 곳에 -10 에..

# 상자 = 숫자
b = a + a  # (20)
# 상자 b 에 a상자의 값(10) + a상자의 값 (10)

a = b  # (20)
# 상자 a 에 b상자의 값(20) 을 넣어요

b = a + a  # (20)
# 상자 b에 a 상자의 값(20) + a 상자의 값(20)
# a = 20, b = 40

b = a + b
# 상자 b 에 a상자의 값 (20) + b상자의 값 (40)
# a = 20, b = 60


print(f"a:{a} , b:{b}")  # a의 값을 출력해줘,보여줘


